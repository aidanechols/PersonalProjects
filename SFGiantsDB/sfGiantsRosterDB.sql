CREATE DATABASE sfGiantsRoster;

USE sfGiantsRoster

CREATE TABLE players(
numberpk smallint NOT NULL PRIMARY KEY,
number smallint NOT NULL,
firstName varchar(50) NOT NULL,
lastName varchar(50),
position varchar(50),
positionID smallint,
Active_Org bit,
Active_40 bit,
Active_26 bit,
);

CREATE TABLE playersData(
numberpk smallint NOT NULL FOREIGN KEY REFERENCES players(numberpk),
number smallint NOT NULL,
firstName varchar(50) NOT NULL,
lastName varchar(50),
position varchar(50),
positionID smallint,
bats varchar(1),
throws varchar(1),
heightFT varchar(5),
weightLBS smallint,
DOB date
);

CREATE TABLE salary(
numberpk smallint NOT NULL FOREIGN KEY REFERENCES players(numberpk),
firstName varchar(50),
lastName varchar(50),
salary int
);

CREATE TABLE freeAgents(
freeAgentID smallint NOT NULL PRIMARY KEY,
firstName varchar(50) NOT NULL,
lastName varchar(50),
position varchar(50),
positionID smallint,
);

CREATE TABLE freeAgentSalary(
freeAgentID smallint NOT NULL FOREIGN KEY REFERENCES freeAgents(freeAgentID),
firstName varchar(50),
lastName varchar(50),
previousTeam varchar(50),
previousSalary int,
projectedSalary int
);

